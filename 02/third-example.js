// Цикл for
for (let i = 0; i < 150; i+=1)
  console.log("Hello World");

// Чтоб сохранить память
let i = 150;
for (i; i > 0; i-=1)
  console.log("Hello World");

// Итерация
// i - инкремент, прибавляется
// i - декремент, отнимается

// While - очень гибкий в плане условия
// For - проверка только чисел

while(true) {
  console.log("Hello World");
}

let i = 150;
// i не равно 0
while (i) {
  console.log("Hello");
  i -= 1;
}

